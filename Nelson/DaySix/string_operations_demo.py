# 示例程序：展示字符串的各种运算符使用方法

# 1. 字符串重复
s1 = 'hello ' * 3
print("重复字符串：", s1)  # 输出： hello hello hello 

# 2. 字符串拼接
s2 = 'world'
s1 += s2
print("拼接字符串：", s1)  # 输出： hello hello hello world

# 3. 成员运算符 in 和 not in
print("检查 'll' 是否在字符串中：", 'll' in s1)  # 输出： True
print("检查 'good' 是否在字符串中：", 'good' in s1)  # 输出： False

# 4. 字符串下标运算（获取指定位置的字符）
str2 = 'abc123456'
print("下标运算，索引2的字符：", str2[2])  # 输出： c

# 5. 字符串切片运算
print("从索引2到5的切片：", str2[2:5])  # 输出： c12
print("从索引2开始到末尾的切片：", str2[2:])  # 输出： c123456
print("从索引2开始，每隔一个字符取一个字符：", str2[2::2])  # 输出： c246
print("从头到尾，每隔一个字符取一个字符：", str2[::2])  # 输出： ac246

# 6. 字符串反转（步长为-1）
print("字符串反转：", str2[::-1])  # 输出： 654321cba

# 7. 使用负数索引切片
print("负数索引切片，取倒数第3到倒数第1个字符：", str2[-3:-1])  # 输出： 45
